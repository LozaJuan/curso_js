//Operadores de asignaci√≥n, incremento y decremento
let a=1;
document.write('el valor de a:',a);
document.write('<br>');

a++; //ASIGNA 1 
document.write('el valor de a:',a);
document.write('<br>');

a+=5; // ASIGNA EL VALOR DESEADO
document.write('el valor de a:',a);
document.write('<br>');

a--; // SUSTRAE 1
document.write('el valor de a:', a);
document.write('<br>');

a-=3; // SUSTRAE EL VALOR DESEADO
document.write('el valor de a:', a);
document.write('<br>');

a*=2; // SACA EL PRODUCTO CON EL VALOR DESEADO
document.write('el valor de a:', a);
document.write('<br>');

a/=3; // SACA EL COCIENTE DEL VALOR ASIGNADO
document.write('el valor de a:', a);
document.write('<br>');

a**=3; // SACA LA POTENCIA AL VALOR DESEADO
document.write('el valor de a:', a);